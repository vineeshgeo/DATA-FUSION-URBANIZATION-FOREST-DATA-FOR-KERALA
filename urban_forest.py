# Function to create fused classification
def createFusedClassification(smod, forest, year):
    """
    Create fused classification combining SMOD and forest data

    Args:
        smod: SMOD image
        forest: Forest mask image
        year: Year string for naming

    Returns:
        Classification image
    """
    # Reclassify SMOD
    # Urban Centre (30) -> 1
    # Urban Cluster (21, 22, 23) -> 2
    # Rural (10, 11, 12, 13) -> 3

    urban = smod.eq(30)
    periUrban = smod.eq(21).Or(smod.eq(22)).Or(smod.eq(23))
    rural = smod.eq(10).Or(smod.eq(11)).Or(smod.eq(12)).Or(smod.eq(13))

    # Create base classification
    classification = (ee.Image(0)
        .where(rural, 3)           # Rural
        .where(periUrban, 2)       # Peri-Urban
        .where(urban, 1)           # Urban
        .where(forest.And(rural.Or(smod.eq(10))), 4)  # Natural Forest
        .clip(aoi)
        .rename(f'classification_{year}'))

    return classification.toByte()

