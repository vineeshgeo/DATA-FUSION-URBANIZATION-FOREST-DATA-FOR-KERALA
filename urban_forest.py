class_names = ['Urban', 'Peri-Urban', 'Rural', 'Natural Forest']

def calculate_area_stats(image, year):
    """
    Calculate area statistics for each classification class

    Args:
        image: Classification image
        year: Year string
    """
    print(f'--- Area Statistics for {year} ---\n')

    results = {}

    for i in range(1, 5):
        classArea = image.eq(i).multiply(ee.Image.pixelArea()).divide(1e6)
        stats = classArea.reduceRegion(**{
            'reducer': ee.Reducer.sum(),
            'geometry': aoi.geometry(),
            'scale': 100,
            'maxPixels': 1e13
        })

        area_value = stats.getInfo().get(f'classification_{year}', 0)
        results[class_names[i-1]] = area_value
        print(f'{class_names[i-1]:<20}: {area_value:>10.2f} sq km')

    print()
    return results

